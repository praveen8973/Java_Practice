class  SumOfN //Sum of N Numbers (for): Calculate the sum of the first 100 natural numbers using a for loop.

{
	public static void main(String[] args) 
	{
		int sum=0;	
			System.out.println(sum);
			for (int i=0;i<=100;i++)
			{
				sum+=i;
				System.out.println(sum);
			}
		//System.out.println("Hello World!");
	}
}
